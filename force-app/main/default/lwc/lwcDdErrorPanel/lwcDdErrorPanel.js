/*****************************************************************************************************************************
 *
 * Imports
 *
 *****************************************************************************************************************************/
import { LightningElement, api } from 'lwc';
import { reduceErrors } from 'c/lwcDdUtils';

export default class LwcDdErrorPanel extends LightningElement {

   /*****************************************************************************************************************************
    *
    * Public Variables
    *
    *****************************************************************************************************************************/
    @api errors;
    /** Generic / user-friendly message */
    @api friendlyMessage = 'Error Occured';

   /*****************************************************************************************************************************
    *
    * Private Variables
    *
    *****************************************************************************************************************************/
    viewDetails = false;

   /*****************************************************************************************************************************
    *
    * UI Getters
    *
    *****************************************************************************************************************************/
    get errorMessages() {
        return reduceErrors(this.errors);
    }

   /*****************************************************************************************************************************
    *
    * Event handlers
    *
    *****************************************************************************************************************************/
    handleShowDetailsClick() {
        this.viewDetails = !this.viewDetails;
    }
}